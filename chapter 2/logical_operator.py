#logical operator 

a=6
b=12

#logical and operator 
#this will give true only when both the conditions or operands are true 
print(a>7 and b>5)


#logical or operator 
#this will give true for all the true present and false if both the operand are false 
print(a>4 or b>5)   #this will give true 
print(a<5 or b<9)  #this will give false
