a=4
print(type(a))  ##this will give integer type 

b="25"
print(type(b))  ## this will give string type 


c=34.24
print(type(c))  ##this will give float as the type 


## type casting 
## this means converting one type to another type 

d="24.7"

print(type(d))  # this will give string as the type 


d=float(d)
print(type(d))  # this will give float as the type 


