# length 
a="saripodha shanivaram"
print(len(a))

#capitalize
print(a.capitalize())  # this will turn the lower case of the first character of complete string to upper case

#title    
print(a.title())   # this will turn the lower case of the first character to upper case 

#upper 
print(a.upper())  # this will turn the complete string to upper case 

#lower 
print(a.lower())  # this will turn the complete string to lower case


# white space trimming or stripping             
b=" saripodha "
print(b.strip())


#string slicing  or sub string 
print(a[0:3])


# replace 
print(a.replace("saripodha","saripothadhi"))

# endswith 
print(a.endswith("ram"))

# startswith
print(a.startswith("sar"))


# index or find
print(a.find("dha"))

print(a.index("a"))

# split  ( this will create the list)

print(a.split())

c="dont!!know"
print(c.split("!!"))

# count 
print(a.count("s"))

# islower
print(a.islower())


